// litrals are only the value which are stored in value
public class _01_Litral {
    public static void main(String[] args) {

        String name = "devill";
        char a = 'T';   // character liral
        byte age = 15; // string litral
        float height = 15.6f;   // by default it take as double so we need to aff f at the last to indicate it if float
        double breadht = 15.9;


        System.out.println(a);
        System.out.println(age);
        System.out.println(height);
        System.out.println(breadht);
        System.out.println(name);

    }
}
